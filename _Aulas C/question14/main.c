#include <stdio.h>
#include <stdlib.h>

int main()
{
    int p, q;
    p=1; q=1;
    printf("Valor de p = %d\n",p);
    printf("Valor de q = %d\n",q);
    printf("\nCriando o incremento: p++\n");
    p++;
    q--;
    printf("\n O novo valor para p = %d\n",p);
    printf("\n O novo valor para q = %d\n",q);
    return 0;
}
